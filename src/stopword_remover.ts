/**
 * Removes commonly occuring words that have no contribution to the semantics of the corpus.
 * @param corpus : Amharic text
 * @returns : the corpus without stopwords listed on {@link stop_word_list}
 *
 * @example {remove stopwords}
 * rmvStopwrd("ይህ ሞባይል እና ኮምፒዩተር") // returns "ሞባይል ኮምፒዩተር"
 */
function removeStopwords(corpus: string) {
  stop_word_list.forEach((word) => {
    let regex = new RegExp(`${word}`)
    corpus = corpus.replace(regex, "")
  })

  return corpus
}

export default removeStopwords
